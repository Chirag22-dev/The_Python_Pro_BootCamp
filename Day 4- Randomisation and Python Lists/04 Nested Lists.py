# List of fruits
fruits = ["apple", "banana", "cherry"]

# List of vegetables
vegetables = ["carrot", "beetroot", "Raddish"]

# Create a nested list (list inside another list)
nest_lists = [fruits, vegetables]

# Print the complete nested list
print(nest_lists)

# Access the 3rd element (index 2) from the first list (fruits)
print(nest_lists[0][2])
